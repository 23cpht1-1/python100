def smallest_unicode_char(string):
    # 문자열에 들어있는 문자의 유니코드를 저장할 리스트 생성
    unicode_list = []
    # 문자열을 순회하며 각 문자의 유니코드를 리스트에 추가
    for char in string:
        unicode_list.append(ord(char))
    # 리스트를 오름차순으로 정렬
    unicode_list.sort()
    # 가장 작은 유니코드 값을 가진 문자 반환
    return chr(unicode_list[0])

